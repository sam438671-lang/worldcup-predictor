def get_team_form(team):
    """回傳近期戰績加成 (-5 ~ +5)"""
    f = RECENT_FORM.get(team)
    if not f: return 0
    wins, draws, losses, gf, ga, opp_elo = f
    # 積分率
    pts_pct = (wins*3 + draws) / max((wins+draws+losses)*3, 1)
    # 得失球差
    gd = gf - ga
    gd_bonus = max(-3, min(3, gd * 0.5))
    # 綜合
    return (pts_pct - 0.5) * 5 + gd_bonus * 0.5

